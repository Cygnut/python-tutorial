"""The "for" loop
For loops iterate over a given sequence. Here's an example:
"""

# %%

primes = [2, 3, 5, 7]
for prime in primes:
    print(prime)

# %%

""""while" loops
While loops repeat as long as a certain boolean condition is met. For example:
"""

# %%

count = 0
while count < 5:
    print(count)
    count += 1  # This is the same as count = count + 1

